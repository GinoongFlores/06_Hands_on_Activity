
public class Person {
    private String name;
    private String ContactNum;

    public void setName(String name){
        this.name = name;
    }

    public void setContactNum(String ContactNum) {
        this.ContactNum = ContactNum;
    }

    public String getName() {
        return name;
    }

    public String getContactNum() {
        return ContactNum;
    }
}
